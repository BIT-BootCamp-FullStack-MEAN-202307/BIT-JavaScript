let result;

result = 3 + 9;             // Suma
result += 9;                // result = result + 9
console.log( result );

result = "3" + 9;             // Concatenacion
console.log( result );

result = 3 - 9;             // Resta
result -= 9;                // result = result - 9
console.log( result );

result = 3 * 9;             // Multiplicacion
result *= 9;                // result = result * 9
console.log( result );

result = 3 / 9;             // Division
result /= 9;                // result = result / 9
console.log( result );

result = 3 % 9;             // Modulo: Residuo de la division
result %= 9;                // result = result % 9
console.log( result );

result = 3 ** 9;            // Exponenciacion
console.log( result );